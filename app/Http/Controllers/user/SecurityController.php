    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $validatedData = $request->validate([
            'cpassword' => 'required|min:4|max:255',
            'password' => 'required|confirmed|min:4|max:255',
        ]);

        // checking if current password is correct
        if (!Hash::check($validatedData['cpassword'], auth()->user()->password)) {
            return redirect()->back()->withErrors('Incorrect Account Password, Please try again');
        }

        // updating new password
        auth()->user()->password = Hash::make($validatedData['password']);
        auth()->user()->save();

        return redirect()->back()->with('success', 'Password Updated Successfully');
    }

